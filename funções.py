### Funções são pequenos pedaçõs de código definidos
# para executar determinada ação ou execução
# As funções servem para executar toda vez que uma ação
# é solicitada, otimizando linhas de código e tempo de execução.
# Evitando linhas de códigos repetitivas.

# Função sem argumento

def oi():
    print("Olá Pessoal")
    
# Chamando a função

oi()

## Função com argumento

def soma(y,z):
    return y+z

## Chamar a função

print(soma(2,3))



    

    